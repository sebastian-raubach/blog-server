package blog.raubach.pojo;

public class YearCount
{
	private int year;
	private int count;

	public int getYear()
	{
		return year;
	}

	public YearCount setYear(int year)
	{
		this.year = year;
		return this;
	}

	public int getCount()
	{
		return count;
	}

	public YearCount setCount(int count)
	{
		this.count = count;
		return this;
	}
}
